    private void btnconnectuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconnectuserActionPerformed

        try {
            Connect();
            pst = con.prepareStatement("select * from utilisateur where matricule=? and password=? and statut=?");
            pst.setString(1, matri.getText());
            pst.setString(2, password.getText());
            pst.setString(3, (String) combo.getSelectedItem());

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String a = (String) combo.getSelectedItem();
                if ("Enseignants".equals(a)) {
                    String code = JOptionPane.showInputDialog(null, "Saisir le code d'acces resverver aux enseignants");
                    if (!code.equals("0852")) {
                        JOptionPane.showMessageDialog(null, "Erreur, veuillez reesayer");
                    } else {
                        JOptionPane.showMessageDialog(null, "Acces Accorder");
                        Operation A = new Operation();
                        A.setVisible(true);
                        this.hide();
                    }

                } else if ("Etudiant".equals(a)) {

                    Note_Tp A = new Note_Tp();
                    A.setVisible(true);
                    this.hide();
                }
                if ("Administration".equals(a)) {
                    String code = JOptionPane.showInputDialog(null, "Saisir le code d'acces resverver a l'administration");
                    if (!code.equals("0851")) {
                        JOptionPane.showMessageDialog(null, "Erreur, veuillez reesayer");
                    } else {
                        JOptionPane.showMessageDialog(null, "Acces Accorder");
                        Operation A = new Operation();
                        A.setVisible(true);
                        this.hide();
                    }

                }

            } else {
                JOptionPane.showMessageDialog(null, "Erreur, veuillez reesayer");
                matri.setText("");
                password.setText("");
            }

        } catch (Exception e) {
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnconnectuserActionPerformed

